<h2 class="course_heading"> What Is Remote Sensing?

 </h2>

 <p class="class_paragraph capitalize">The measurement of the earth using sensors is known as remote sensing. The energy that Earth reflects back is typically detected by remote sensors to gather data. Aircraft and satellites both have these sensors in place. In order to analyze and visualize the data, remote sensors alter the images that they collect as data in the form of images. Given that GIS systems use remote sensors to visualize and analyze the data they collect, there is a relationship between GIS and remote sensors.

</p>

